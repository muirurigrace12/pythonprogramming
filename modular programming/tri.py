import math
def sin(a):
    angle_rad = math.radians(a)
    angle_sin =math.sin(angle_rad)
    return angle_sin

def tan(a):
    angle_rad = math.radians(a)
    angle_tan = math.tan(angle_rad)
    return angle_tan

def cos(a):
    angle_rad = math.radians(a)
    angle_cos = math.cos(angle_rad)
    return angle_cos

